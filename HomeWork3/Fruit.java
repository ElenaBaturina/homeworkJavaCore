package HomeWork3;

public class Fruit {
    private int quantity;
    private int weight;

    public Fruit (int weight) {
        this.weight = weight;
    }

    public int getQuantity() {
        return quantity;
    }

    public float getWeight() {
        return weight;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
